#!/usr/bin/env node
import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';

const [input, output] = process.argv.slice(2);
if (input === undefined || output === undefined) {
  throw new Error('usage: bundle-wasm2js.mjs INPUT.mjs OUTPUT.mjs');
}

const importPrefix = [
  "import * as teavm from 'teavm';",
  "import * as teavmMath from 'teavmMath';",
  "import * as env from 'env';",
  '',
].join('\n');

const inlineImports = `// Generated by bundle-wasm2js.mjs. These are the only imports emitted by
// TeaVM 0.13.1 for the isolated, single-thread authority reachable set.
const teavm = Object.freeze({
  logString() {},
  logInt() {},
  logOutOfMemory() { throw new Error('TeaVM out of memory'); },
  putwcharsErr() {},
  putwcharsOut() {},
  currentTimeMillis() { return Date.now(); },
  nanoTime() {
    return typeof performance === 'object' && performance !== null
      ? performance.now() * 1e6
      : Date.now() * 1e6;
  },
});
const teavmMath = Math;
let tempRet0 = 0;
const env = Object.freeze({
  setTempRet0(value) { tempRet0 = value | 0; },
});
export function wasm2jsTempRet0() { return tempRet0; }

`;

const source = fs.readFileSync(input, 'utf8');
if (!source.startsWith(importPrefix)) {
  throw new Error('unexpected wasm2js import surface; refusing to bundle');
}
const remainder = source.slice(importPrefix.length);
if (/^import\s/m.test(remainder)) {
  throw new Error('unexpected additional wasm2js import; refusing to bundle');
}

fs.mkdirSync(path.dirname(output), {recursive: true});
fs.writeFileSync(output, inlineImports + remainder);
